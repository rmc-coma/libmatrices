#include "libmatrices.h"

t_vec3	*m_vec4to3(t_vec4 *src, t_vec3 *dst)
{
	dst->vec[0] = src->vec[0];
	dst->vec[1] = src->vec[1];
	dst->vec[2] = src->vec[2];
	return (dst);
}
